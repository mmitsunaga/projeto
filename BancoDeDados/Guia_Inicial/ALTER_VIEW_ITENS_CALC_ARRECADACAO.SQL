CREATE OR REPLACE FORCE VIEW PROJUDI.VIEW_ITENS_CALC_ARRECADACAO AS 
  SELECT 
    TGC.ID_GUIA_CUSTA_MODELO AS ID_GUIA_CUSTA_MODELO,
    TGC.GUIA_CUSTA_MODELO AS GUIA_CUSTA_MODELO,
    TG.ID_GUIA_MODELO AS ID_GUIA_MODELO,
    TG.GUIA_MODELO AS GUIA_MODELO,
    TG.ID_GUIA_TIPO AS ID_GUIA_TIPO,
    TG.ID_PROC_TIPO AS ID_PROC_TIPO,
	TG.ID_NATUREZA_SPG AS ID_NATUREZA_SPG,
    C.ID_CUSTA AS ID_CUSTA,
    C.CUSTA AS CUSTA,
    C.CODIGO_REGIMENTO AS CODIGO_REGIMENTO,
    C.CODIGO_REGIMENTO_VALOR AS CODIGO_REGIMENTO_VALOR,
    C.PORCENTAGEM AS PORCENTAGEM,
    C.REFERENCIA_CALCULO AS REFERENCIA_CALCULO,
    C.MINIMO AS MINIMO,
    C.VALOR_ACRESCIMO AS VALOR_ACRESCIMO,
    C.VALOR_MAXIMO AS VALOR_MAXIMO,
    AC.ID_ARRECADACAO_CUSTA AS ID_ARRECADACAO_CUSTA,
    AC.ARRECADACAO_CUSTA AS ARRECADACAO_CUSTA,
    AC.CODIGO_ARRECADACAO AS CODIGO_ARRECADACAO 
FROM GUIA_CUSTA_MODELO TGC JOIN GUIA_MODELO TG ON TGC.ID_GUIA_MODELO = TG.ID_GUIA_MODELO
     JOIN CUSTA C ON TGC.ID_CUSTA = C.ID_CUSTA 
	 JOIN ARRECADACAO_CUSTA AC ON C.ID_ARRECADACAO_CUSTA = AC.ID_ARRECADACAO_CUSTA;
