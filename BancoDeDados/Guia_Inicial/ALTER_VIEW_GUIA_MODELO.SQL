CREATE OR REPLACE FORCE VIEW PROJUDI.VIEW_GUIA_MODELO AS 
  SELECT 
    GC.ID_GUIA_MODELO AS ID_GUIA_MODELO,
    GC.GUIA_MODELO AS GUIA_MODELO,
    GC.GUIA_MODELO_CODIGO AS GUIA_MODELO_CODIGO,
    GT.ID_GUIA_TIPO AS ID_GUIA_TIPO,
    GT.GUIA_TIPO AS GUIA_TIPO,
    GT.FLAG_GRAU AS FLAG_GRAU,
    PT.ID_PROC_TIPO AS ID_PROC_TIPO,
    PT.PROC_TIPO AS PROC_TIPO,
	NS.ID_NATUREZA_SPG AS ID_NATUREZA_SPG,
    NS.NATUREZA_SPG AS NATUREZA_SPG,
	GC.ACRESCIMO_LOCOMOCAO AS ACRESCIMO_LOCOMOCAO,
	GC.PENHORA_LOCOMOCAO AS PENHORA_LOCOMOCAO,
    GC.CODIGO_TEMP AS CODIGO_TEMP 
  FROM GUIA_MODELO GC LEFT JOIN PROC_TIPO PT ON GC.ID_PROC_TIPO = PT.ID_PROC_TIPO
  LEFT JOIN NATUREZA_SPG NS ON GC.ID_NATUREZA_SPG = NS.ID_NATUREZA_SPG
  INNER JOIN GUIA_TIPO GT ON GC.ID_GUIA_TIPO = GT.ID_GUIA_TIPO;
