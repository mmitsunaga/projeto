CREATE OR REPLACE FORCE VIEW PROJUDI.VIEW_BAIRRO AS 
SELECT
    B.ID_BAIRRO AS ID_BAIRRO,
    B.BAIRRO AS BAIRRO,
    B.BAIRRO_CODIGO AS BAIRRO_CODIGO,
    B.ID_CIDADE AS ID_CIDADE,
    C.CIDADE AS CIDADE,
    B.CODIGO_TEMP AS CODIGO_TEMP,
    C.UF AS UF ,
	C.CODIGO_SPG AS CODIGO_CIDADE_SPG,
    B.CODIGO_SPG as CODIGO_SPG,
    Z.ID_ZONA AS ID_ZONA,
    Z.ZONA AS ZONA,
    R.ID_REGIAO AS ID_REGIAO,
    R.REGIAO AS REGIAO
FROM
    BAIRRO B JOIN VIEW_CIDADE C ON B.ID_CIDADE = C.ID_CIDADE
    LEFT JOIN ZONA_BAIRRO_REGIAO ZBR ON ZBR.ID_BAIRRO = B.ID_BAIRRO
    LEFT JOIN ZONA Z ON Z.ID_ZONA = ZBR.ID_ZONA
    LEFT JOIN REGIAO R ON R.ID_REGIAO = ZBR.ID_REGIAO;